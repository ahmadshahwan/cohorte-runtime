# Auto Builder code package
#
# Description from: https://code.google.com/p/auto-builder/
#
# Auto Builder is an Ant build script generator for OSGi projects.
# It complements the Eclipse Equinox Plugin Development Environment (PDE),
# such that all development can be done in PDE, and the PDE artifacts are
# resused for the automation environment.
# 
